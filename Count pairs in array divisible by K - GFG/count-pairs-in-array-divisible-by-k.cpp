class Solution
{
    public:
    long long countKdivPairs(int A[], int n, int k)
    {
        //code here
        long long res=0;
        unordered_map<int,int> mp;
        for(int i=0;i<n;i++){
            int rem = A[i]%k;
            res+=mp[(k-rem)%k];
            mp[rem]++;
        }
        
        return res;
    }
};

